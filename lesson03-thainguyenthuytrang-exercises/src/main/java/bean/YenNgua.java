package bean;

public class YenNgua {
	private int Hang;
	private int Cot;

	public YenNgua() {
	}

	public YenNgua(int hang, int cot) {
		super();
		Hang = hang;
		Cot = cot;
	}

	public int getHang() {
		return Hang;
	}

	public void setHang(int hang) {
		Hang = hang;
	}

	public int getCot() {
		return Cot;
	}

	public void setCot(int cot) {
		Cot = cot;
	}

}
